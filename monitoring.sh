#!/bin/bash

# ============================================
# Скрипт для просмотра номерных директорий в /proc
# ============================================

echo "=== Просмотр директории /proc ==="
echo "Поиск номерных директорий (PID процессов)..."
echo ""

# Счетчик найденных процессов
count=0

# Перебираем все директории в /proc
for dir in /proc/*/; do
    # Получаем имя директории без пути
    dir_name=$(basename "$dir")
    
    # Проверяем, является ли имя директории числом
    if [[ $dir_name =~ ^[0-9]+$ ]]; then
        echo "Найдена номерная директория: $dir_name (PID: $dir_name)"
        ((count++))
    fi
done

echo ""
echo "=== Итог ==="
echo "Всего найдено номерных директорий: $count"

# Альтернативный, более компактный способ:
echo ""
echo "=== Альтернативный вывод (списком) ==="
ls -d /proc/[0-9]*/ 2>/dev/null | sed 's/\/$//' | sed 's/\/proc\///'
